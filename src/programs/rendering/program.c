void _start(uint64_t pid)
{
    id = pid;
    cores = getCores();
    texture = allocate(64 * 64 * sizeof(EFI_GRAPHICS_OUTPUT_BLT_PIXEL));
    readBitmap(readFile(L"programs/rendering/wall.bmp", NULL), texture);
    window = allocateWindow(640, 360, L"Game", L"programs/rendering/program.bmp");
    window->mouseMode = 2;
}

void move(float* x, float* y, float moveX, float moveY, float speed)
{
    float magnitude = sqrt(moveX * moveX + moveY * moveY);
    moveX = (moveX / magnitude) * speed;
    moveY = (moveY / magnitude) * speed;
    float newX = *x + moveX;
    if (map[(uint8_t)(*y)][(uint8_t)(newX)] == 0)
    {
        *x = newX;
    }
    float newY = *y + moveY;
    if (map[(uint8_t)(newY)][(uint8_t)(*x)] == 0)
    {
        *y = newY;
    }
}

void coreBackground(uint64_t id)
{
    for (uint32_t y = 0; y < window->height / cores; y++)
    {
        uint32_t line = y + id * (window->height / cores);
        uint8_t brightness = 0;
        if (line < halfHeight)
        {
            brightness = 128 - (uint8_t)(line / (float)halfHeight * 128);
        }
        else
        {
            brightness = (uint8_t)((line - halfHeight) / (float)halfHeight * 128);
        }
        EFI_GRAPHICS_OUTPUT_BLT_PIXEL pixel;
        pixel.Red = brightness;
        pixel.Green = brightness;
        pixel.Blue = brightness;
        for (uint32_t x = 0; x < window->width; x++)
        {
            window->buffer[line * window->width + x] = pixel;
        }
    }
}

void coreRender(uint64_t id)
{
    for (int64_t x = 0; x < window->width / cores; x++)
    {
        int64_t line = x + id * (window->width / cores);
        float raycastX = playerX;
        float raycastY = playerY;
        float raycastDirection = direction + atan((line - halfWidth) / distribution);
        while (map[(uint8_t)(raycastY)][(uint8_t)(raycastX)] != 1)
        {
            float raySpeed = 1.0f;
            if (map[(uint8_t)(raycastY)][(uint8_t)(raycastX)] == 2)
            {
                raySpeed = 0.01f;
            }
            raycastX += cos(raycastDirection) * raySpeed;
            raycastY += sin(raycastDirection) * raySpeed;
        }
        float distance = sqrt((raycastX - playerX) * (raycastX - playerX) + (raycastY - playerY) * (raycastY - playerY));
        uint64_t wallHeight = (uint64_t)(distribution * 4 / (distance * cos(raycastDirection - direction)));
        uint64_t halfWallHeight = wallHeight / 2;
        float uncappedBrightness = 1.5f - distance / 15.0f;
        float brightness = max(min(uncappedBrightness, 1.0f), 0.0f);
        for (uint64_t y = 0; y < wallHeight; y++)
        {
            uint32_t yPixel = halfHeight - halfWallHeight + y;
            if (yPixel >= 0 && yPixel < window->height)
            {
                EFI_GRAPHICS_OUTPUT_BLT_PIXEL colour = texture[(uint8_t)((float)(y) / wallHeight * 64) * 64 + (uint8_t)((int64_t)((raycastX + raycastY) * 10.0f) % 40 / 40.0f * 64)];
                EFI_GRAPHICS_OUTPUT_BLT_PIXEL pixel;
                pixel.Red = brightness * colour.Red;
                pixel.Green = brightness * colour.Green;
                pixel.Blue = brightness * colour.Blue;
                window->buffer[yPixel * window->width + line] = pixel;
            }
        }
    }
}

void update(uint64_t ticks)
{
    Event* event = (Event*)&window->events;
    while (iterateList((void**)&event))
    {
        switch (event->id)
        {
            case 0:
                unallocateWindow(window);
                unallocate(texture);
                quit(id);
                break;
            case 1:
                switch (((KeyEvent*)event)->scancode)
                {
                    case 16:
                        q = ((KeyEvent*)event)->pressed;
                        break;
                    case 18:
                        e = ((KeyEvent*)event)->pressed;
                        break;
                    case 17:
                        w = ((KeyEvent*)event)->pressed;
                        break;
                    case 30:
                        a = ((KeyEvent*)event)->pressed;
                        break;
                    case 31:
                        s = ((KeyEvent*)event)->pressed;
                        break;
                    case 32:
                        d = ((KeyEvent*)event)->pressed;
                        break;
                    case 42:
                        shift = ((KeyEvent*)event)->pressed;
                        break;
                }
                break;
            case 3:
                direction += ((MouseEvent*)event)->x * 0.002f;
                if (direction < 0)
                {
                    direction += 6.28f;
                }
                else if (direction > 6.28f)
                {
                    direction -= 6.28f;
                }
                break;
        }
        removeItem((void**)&window->events, event);
        event = (Event*)&window->events;
    }
    if (q)
    {
        direction -= 0.003f * ticks;
        if (direction < 0)
        {
            direction += 6.28f;
        }
    }
    if (e)
    {
        direction += 0.003f * ticks;
        if (direction > 6.28f)
        {
            direction -= 6.28f;
        }
    }
    float speed = 0.005f;
    if (shift)
    {
        speed *= 2;
    }
    float moveX = 0.0f;
    float moveY = 0.0f;
    if (w && !s)
    {
        moveX += cos(direction);
        moveY += sin(direction);
    }
    if (a && !d)
    {
        float moveDirection = direction + 1.57f;
        moveX -= cos(moveDirection);
        moveY -= sin(moveDirection);
    }
    if (s && !w)
    {
        moveX -= cos(direction);
        moveY -= sin(direction);
    }
    if (d && !a)
    {
        float moveDirection = direction + 1.57f;
        moveX += cos(moveDirection);
        moveY += sin(moveDirection);
    }
    move(&playerX, &playerY, moveX, moveY, speed * ticks);
    map[(uint8_t)(enemyY)][(uint8_t)(enemyX)] = 0;
    move(&enemyX, &enemyY, playerX - enemyX, playerY - enemyY, 0.005f * ticks);
    map[(uint8_t)(enemyY)][(uint8_t)(enemyX)] = 1;
    halfHeight = window->height / 2;
    splitTask(coreBackground, cores);
    halfWidth = window->width / 2;
    distribution = halfWidth / tan(1.57f / 2.0f);
    splitTask(coreRender, cores);
}
